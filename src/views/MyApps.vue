<template>
  <Content>
    <PageTitle
      title="My Apps"
      class="mb-6"
    >
      <template #right>
        <KButton
          data-testid="create-application-button"
          appearance="primary"
          :is-rounded="false"
          :to="{ name: 'create-application' }"
        >
          <svg
            width="16"
            height="16"
            fill="none"
            xmlns="http://www.w3.org/2000/svg"
            class="mr-2"
          >
            <title>Plus</title>
            <path
              fill-rule="evenodd"
              clip-rule="evenodd"
              d="M16 8A8 8 0 110 8a8 8 0 0116 0zM7 5a1 1 0 012 0v2h2a1 1 0 110 2H9v2a1 1 0 11-2 0V9H5a1 1 0 110-2h2V5z"
              fill="#fff"
              fill-opacity=".75"
            />
          </svg>
          New App
        </KButton>
      </template>
    </PageTitle>
    <div>
      <KCard>
        <template #body>
          <KTable
            data-testid="applications-table"
            :fetcher-cache-key="fetcherCacheKey"
            :fetcher="fetcher"
            has-side-border
            :has-error="currentState.matches('error')"
            :is-loading="currentState.matches('pending')"
            :headers="tableHeaders"
            is-clickable
            is-small
            class="applications-table"
            :pagination-page-sizes="paginationConfig.paginationPageSizes"
            :initial-fetcher-params="{ pageSize: paginationConfig.initialPageSize }"
            @row:click="(_, row) => $router.push({ name: 'show-application', params: { application_id: row.id }})"
          >
            <template #name="{ row }">
              {{ row.name }}
            </template>
            <template #actions="{ row }">
              <ActionsDropdown :key="row.id">
                <template #content>
                  <div
                    v-if="isDcr"
                    data-testid="dropdown-refresh-application-dcr-token"
                    class="py-2 px-3 type-md cursor-pointer"
                    @click="handleRefreshSecret(row.id)"
                  >
                    Refresh secret
                  </div>
                  <div
                    data-testid="dropdown-delete-application"
                    class="py-2 px-3 type-md cursor-pointer delete-item"
                    @click="deleteItem = row"
                  >
                    Delete
                  </div>
                </template>
              </ActionsDropdown>
            </template>
            <template #empty-state>
              <EmptyState
                title="No Applications"
              >
                <template #message>
                  <p>
                    <router-link
                      data-testid="create-application-link"
                      :to="{
                        name: 'create-application'
                      }"
                    >
                      Create a new app
                    </router-link>
                    to get started
                  </p>
                </template>
              </EmptyState>
            </template>
            <template #error-state>
              <EmptyState
                is-error
                title="No Applications"
              >
                <template #message>
                  <p>
                    {{ 'Error: ' + errorMessage }}
                  </p>
                </template>
              </EmptyState>
            </template>
          </KTable>
        </template>
      </KCard>
    </div>

    <KModal
      :title="modalTitle"
      :is-visible="!!deleteItem"
      action-button-text="Delete"
      action-button-appearance="danger"
      class="delete-modal"
      @canceled="deleteItem = null"
    >
      <template #header-content>
        {{ modalTitle }}
      </template>
      <template #body-content>
        Are you sure you want to delete {{ deleteItem.name }}? This action cannot be undone.
      </template>
      <template #footer-content>
        <KButton
          appearance="danger"
          :is-rounded="false"
          class="mr-3"
          @click="handleDelete"
        >
          Delete
        </KButton>
        <KButton
          appearance="secondary"
          :is-rounded="false"
          @click="deleteItem = null"
        >
          Cancel
        </KButton>
      </template>
    </KModal>

    <RefreshTokenModal
      :is-visible="showSecretModal"
      :token="token"
      @closed="onModalClose"
    />
  </Content>
</template>

<script lang="ts">
import { defineComponent, computed, ref } from 'vue'
import { useMachine } from '@xstate/vue'
import { createMachine } from 'xstate'
import getMessageFromError from '@/helpers/getMessageFromError'
// TODO: remove `ts-ignore` once typescript is enabled across the project
// @ts-ignore
import RefreshTokenModal from '../components/RefreshTokenModal'
// @ts-ignore
import PageTitle from '../components/PageTitle'
// @ts-ignore
import ActionsDropdown from '../components/ActionsDropdown'
import usePortalApi from '@/hooks/usePortalApi'
import useToaster from '@/composables/useToaster'
import { useAppStore } from '@/stores'
import { storeToRefs } from 'pinia'

export default defineComponent({
  name: 'MyApps',
  components: { PageTitle, ActionsDropdown, RefreshTokenModal },

  setup () {
    const { notify } = useToaster()
    const errorMessage = ref('')
    const applications = ref([])
    const key = ref(0)
    const fetcherCacheKey = computed(() => key.value.toString())
    const deleteItem = ref(null)
    const showSecretModal = ref(false)
    const token = ref(null)
    const { portalApiV2 } = usePortalApi()

    const appStore = useAppStore()
    const { isDcr } = storeToRefs(appStore)

    const paginationConfig = ref({
      paginationPageSizes: [25, 50, 100],
      initialPageSize: 25
    })

    const modalTitle = computed(() => `Delete ${deleteItem.value?.name}`)

    const { state: currentState, send } = useMachine(createMachine({
      predictableActionArguments: true,
      id: 'DeveloperMachine',
      initial: 'idle',
      states: {
        idle: { on: { FETCH: 'pending', REJECT: 'error' } },
        pending: { on: { RESOLVE: 'success', REJECT: 'error' } },
        success: { type: 'final' },
        error: { on: { FETCH: 'pending' } }
      }
    }))

    const revalidate = () => {
      key.value += 1
    }

    const fetcher = async (payload: { pageSize: number; page: number }) => {
      const { pageSize, page: pageNumber } = payload
      const reqPayload = { pageNumber, pageSize }

      send('FETCH')

      return portalApiV2.value.service.applicationsApi
        .getManyApplications(reqPayload)
        .then((res) => {
          send('RESOLVE')

          return {
            data: res.data.data,
            total: res.data.meta.page.total
          }
        }).catch((e) => {
          send('REJECT')

          errorMessage.value = getMessageFromError(e)
        })
    }

    const handleDelete = () => {
      portalApiV2.value.service.applicationsApi
        .deleteApplication({
          applicationId: deleteItem.value.id
        })
        .then(() => {
          deleteItem.value = null
          revalidate() // refetch applications
          notify({
            message: 'Application deleted'
          })
        })
        .catch(error => {
          deleteItem.value = null
          notify({
            appearance: 'danger',
            message: `Error: ${getMessageFromError(error)}`
          })
        })
    }

    const handleRefreshSecret = (id: string) => {
      portalApiV2.value.service.credentialsApi.refreshApplicationToken({ applicationId: id })
        .then((res) => {
          notify({
            message: 'Successfully refreshed secret'
          })
          showSecretModal.value = true
          token.value = res.data.client_secret
        })
        .catch(error => {
          notify({
            appearance: 'danger',
            message: getMessageFromError(error)
          })
        })
    }

    const onModalClose = () => {
      showSecretModal.value = false
      token.value = null
    }

    const tableHeaders = [
      { label: 'Name', key: 'name' },
      { label: 'Description', key: 'description' },
      { hideLabel: true, key: 'actions' }
    ]

    return {
      modalTitle,
      errorMessage,
      applications,
      currentState,
      tableHeaders,
      handleDelete,
      isDcr,
      deleteItem,
      showSecretModal,
      token,
      onModalClose,
      handleRefreshSecret,
      fetcherCacheKey,
      fetcher,
      paginationConfig
    }
  }
})
</script>

<style lang="scss">
